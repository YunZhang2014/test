package abc;

public class ParamExample {
    private final String a;
    private final String b;
    private final String ddd;

    public ParamExample(String a, String b, String ddd) {
        this.a = a;
        this.b = b;
        this.ddd = ddd;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getDdd() {
        return ddd;
    }
}
